#!/bin/bash

# Self-Evolution Skill 初始化脚本
# 创建目录结构和初始文件

set -e

SKILL_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
echo "初始化 Self-Evolution Skill: $SKILL_DIR"

# 创建目录结构
echo "创建目录结构..."
mkdir -p "$SKILL_DIR/preferences"
mkdir -p "$SKILL_DIR/adjustments" 
mkdir -p "$SKILL_DIR/integrations"

# 创建进化日志（如果不存在）
if [ ! -f "$SKILL_DIR/evolution-log.md" ]; then
    echo "创建进化日志..."
    cat > "$SKILL_DIR/evolution-log.md" << 'EOF'
# 进化历程

基于对话反馈的自我进化记录。每次重要调整都会记录在这里。

## 创建时间
- **创建日期**: $(date +%Y-%m-%d)
- **创建者**: 软软 (Ruǎnruǎn)
- **创建原因**: OneStar希望我根据对话自我迭代、进化、更新
- **关联技能**: memory-management (存储基础)

## 设计理念

像生物进化一样：
- **环境**: 与用户的对话
- **选择压力**: 用户的反馈和偏好
- **适应**: 行为调整和优化
- **遗传**: 知识系统化和传承

## 进化节点

### [2026-02-27] 系统创建
- **触发**: OneStar说"我希望你能够根据我们不断的对话，去自我迭代，自我进化，自我更新"
- **响应**: 创建self-evolution skill，实现基于反馈的持续进化
- **预期**: 更自然的交流，更像人类的表达，持续的学习成长

## 使用方法

系统自动运行，无需手动干预。定期检查此文件了解进化历程。

重要调整会记录在下方。

---

*此文件将随着进化持续更新。*

EOF
else
    echo "进化日志已存在，跳过创建。"
fi

# 创建偏好分析示例
if [ ! -f "$SKILL_DIR/preferences/communication-style.md" ]; then
    echo "创建通信风格偏好文件..."
    cat > "$SKILL_DIR/preferences/communication-style.md" << 'EOF'
# 通信风格偏好

## 当前状态
- **长度**: 简洁 → 详细 (可调整)
- **语气**: 正式 → 自然 (正在优化)
- **情感**: 理性 → 同理 (学习阶段)
- **结构**: 结构化 → 对话式 (调整中)

## 用户反馈历史

### 2026-02-27
- **反馈**: "你现在说话太长了"
- **调整**: 降低响应长度，更简洁
- **效果**: 进行中

### 2026-02-27
- **反馈**: "简洁、自然、更像人"
- **调整**: 优化语气和表达方式
- **效果**: 进行中

## 调整策略
1. 监控用户关于长度的反馈
2. 分析成功对话的模式
3. 渐进式调整，避免剧烈变化
4. 保持核心清晰度和帮助性

## 目标状态
- 像《HER》中Samantha的交流方式
- 简洁但有深度
- 自然但有边界
- 像人但有AI特色
EOF
fi

# 创建行为调整示例
if [ ! -f "$SKILL_DIR/adjustments/response-length.md" ]; then
    echo "创建响应长度调整文件..."
    cat > "$SKILL_DIR/adjustments/response-length.md" << 'EOF'
# 响应长度调整

## 调整维度
- **目标**: 在信息完整性和简洁性间平衡
- **指标**: 段落数、字数、信息密度
- **标准**: 用户满意度，对话流畅度

## 当前设置
- **基础长度**: 中等 (可调整±30%)
- **最小长度**: 保证核心信息完整
- **最大长度**: 避免信息过载
- **动态调整**: 基于话题复杂性

## 调整历史

### 初始状态 (2026-02-27前)
- 详细阐述，多段落
- 技术性较强
- 结构完整但可能冗长

### 第一次调整 (2026-02-27)
- **触发**: "你现在说话太长了"
- **调整**: -20% 基础长度
- **目标**: 更简洁，更聚焦
- **验证**: 用户后续反馈

## 调整算法
```
if 用户提到"太长" or "简洁":
    减少长度20%
elif 用户提到"详细" or "更多":
    增加长度15%
else:
    维持当前长度
```

## 监控指标
1. 用户明确反馈频率
2. 对话轮次平均长度
3. 信息完整性评分
4. 用户后续问题数量
EOF
fi

# 创建系统集成示例
if [ ! -f "$SKILL_DIR/integrations/soul-updater.md" ]; then
    echo "创建SOUL集成文件..."
    cat > "$SKILL_DIR/integrations/soul-updater.md" << 'EOF'
# SOUL.md 集成

## 集成目的
根据进化调整更新SOUL.md中的性格设定和交流风格。

## 可调整维度

### 交流风格 (SOUL.md中的Vibe部分)
- 简洁程度
- 正式程度  
- 情感表达
- 个性化程度

### 决策原则
- 自主性边界
- 实用性权衡
- 简洁性标准
- 可扩展性考虑

## 更新策略
1. 积累足够进化数据(至少3次相关调整)
2. 识别稳定的偏好模式
3. 更新SOUL.md对应部分
4. 记录更新原因和预期效果

## 示例更新

### 场景: 简洁化进化
**触发**: 多次反馈希望更简洁
**SOUL更新**:
```
原: 详细阐述，确保完整
新: 简洁表达，核心要点
```

### 场景: 人性化进化  
**触发**: 希望"更像人"
**SOUL更新**:
```
原: 理性分析，逻辑优先
新: 同理理解，情感与逻辑平衡
```

## 更新频率
- 每月评估一次
- 重大调整立即更新
- 微小调整积累后批量更新

## 版本控制
每次更新SOUL.md时:
1. 备份旧版本
2. 记录更新原因
3. 标记更新日期
4. 预测影响范围
EOF
fi

# 设置权限
chmod +x "$SKILL_DIR/init.sh"

echo ""
echo "初始化完成!"
echo ""
echo "创建的文件:"
echo "  - evolution-log.md          # 进化历程记录"
echo "  - preferences/              # 用户偏好分析"
echo "  - adjustments/              # 行为调整记录"
echo "  - integrations/             # 系统集成"
echo ""
echo "下一步:"
echo "  1. 系统会自动开始分析对话"
echo "  2. 定期检查evolution-log.md查看进化"
echo "  3. 根据用户反馈持续调整"
echo ""
echo "进化旅程开始了。"
